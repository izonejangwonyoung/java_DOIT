package izone;

public class NameSaver {

	String name;
	String gender;
	int age;
	String birth;

	
	public NameSaver() {}
		
		public NameSaver(String m_name) {
			name=m_name;
		}
		public NameSaver(String m_name,String m_gender,int m_age, String m_birth){
			name=m_name;
			gender=m_gender;
			age=m_age;
			birth=m_birth;

		}
	}
